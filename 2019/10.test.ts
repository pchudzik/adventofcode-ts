import { expect } from '@jest/globals';
import { part1 } from './10';

describe('10.ts', () => {
  describe('part1', () => {
    it('sample 1', () => {
      const puzzle = `
        .#..#
        .....
        #####
        ....#
        ...##
      `;

      expect(part1(puzzle)).toBe(8);
    });

    it('sample 2', () => {
      const puzzle = `
        ......#.#.
        #..#.#....
        ..#######.
        .#.#.###..
        .#..#.....
        ..#....#.#
        #..#....#.
        .##.#..###
        ##...#..#.
        .#....####
      `;

      expect(part1(puzzle)).toBe(33);
    });

    it('sample 3', () => {
      const puzzle = `
        #.#...#.#.
        .###....#.
        .#....#...
        ##.#.#.#.#
        ....#.#.#.
        .##..###.#
        ..#...##..
        ..##....##
        ......#...
        .####.###.
      `;

      expect(part1(puzzle)).toBe(35);
    });

    it('sample 4', () => {
      const puzzle = `
        .#..#..###
        ####.###.#
        ....###.#.
        ..###.##.#
        ##.##.#.#.
        ....###..#
        ..#.#..#.#
        #..#.#.###
        .##...##.#
        .....#.#..
      `;

      expect(part1(puzzle)).toBe(41);
    });

    it('sample 5', () => {
      const puzzle = `
        .#..##.###...#######
        ##.############..##.
        .#.######.########.#
        .###.#######.####.#.
        #####.##.#.##.###.##
        ..#####..#.#########
        ####################
        #.####....###.#.#.##
        ##.#################
        #####.##.###..####..
        ..######..##.#######
        ####.##.####...##..#
        .#####..#.######.###
        ##...#.##########...
        #.##########.#######
        .####.#.###.###.#.##
        ....##.##.###..#####
        .#.#.###########.###
        #.#.#.#####.####.###
        ###.##.####.##.#..##
      `;

      expect(part1(puzzle)).toBe(210);
    });
  })
});
